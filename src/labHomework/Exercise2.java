package labHomework;
import java.util.Scanner;

public class Exercise2 {
	
	public static void flipLines(Scanner sc)
	{
		while(sc.hasNextLine())
		{
			String x = sc.nextLine();
			
			if (sc.hasNextLine())
			{
				System.out.println(sc.hasNextLine());
			}
			
			System.out.println(x);
			
		}
		
	}

}
